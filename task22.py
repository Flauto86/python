# Задача 22: Даны два неупорядоченных набора целых чисел (может быть, с повторениями).
# Выдать без повторений в порядке возрастания все те числа, которые встречаются в обоих наборах.
# Пользователь вводит 2 числа. n — кол-во элементов первого множества. m — кол-во элементов второго множества.
# Затем пользователь вводит сами элементы множеств.

def find_common_elements(n, m):
    # Ввод элементов первого и второго множества
    set1 = set(map(int, input("Введите элементы первого множества через пробел: ").split()))
    set2 = set(map(int, input("Введите элементы второго множества через пробел: ").split()))

    # Находим пересечение множеств (общие элементы)
    common_elements = sorted(set1.intersection(set2))

    return common_elements

# Ввод количества элементов в первом и втором множествах
n = int(input("Введите количество элементов в первом множестве: "))
m = int(input("Введите количество элементов во втором множестве: "))

result = find_common_elements(n, m)
print("Числа, которые встречаются в обоих наборах:", result)
